const setCaseText = (selector, value) => {
  document.querySelectorAll(selector).forEach((element) => {
    element.textContent = value;
  });
};

const renderCaseStudy = (language = "en") => {
  const requested = new URLSearchParams(window.location.search).get("project");
  const slug = CASE_STUDIES[requested] ? requested : "apex-motorsport";
  const project = CASE_STUDIES[slug];
  const locale = language === "de" ? "de" : "en";
  const content = project[locale];
  const labels = CASE_LABELS[locale];

  document.querySelectorAll("[data-case-label]").forEach((element) => {
    const value = labels[element.dataset.caseLabel];
    if (!value) return;
    if (value.includes("<")) element.innerHTML = value;
    else element.textContent = value;
  });

  setCaseText("[data-case-title]", content.title);
  setCaseText("[data-case-kind]", content.kind);
  setCaseText("[data-case-category]", content.category);
  setCaseText("[data-case-year]", content.year);
  setCaseText("[data-case-summary]", content.summary);
  setCaseText("[data-case-role]", content.role);
  setCaseText("[data-case-technology]", content.technology);
  setCaseText("[data-case-status]", content.status);
  setCaseText("[data-case-challenge]", content.challenge);
  setCaseText("[data-case-role-detail]", content.roleDetail);
  setCaseText("[data-case-process]", content.process);
  setCaseText("[data-case-result]", content.result);
  setCaseText("[data-case-evidence]", content.evidence);
  setCaseText("[data-case-domain]", project.domain);

  document
    .querySelectorAll("[data-case-image], [data-case-image-copy]")
    .forEach((image) => {
      image.src = project.image;
      image.alt = project.alt;
    });

  const decisions = document.querySelector("[data-case-decisions]");
  if (decisions) {
    decisions.replaceChildren(
      ...content.decisions.map(([title, copy], index) => {
        const article = document.createElement("article");
        article.className = "case-decision reveal is-visible";
        const number = document.createElement("span");
        number.textContent = String(index + 1).padStart(2, "0");
        const heading = document.createElement("h3");
        heading.textContent = title;
        const paragraph = document.createElement("p");
        paragraph.textContent = copy;
        article.append(number, heading, paragraph);
        return article;
      }),
    );
  }

  const live = document.querySelector("[data-case-live]");
  if (live) live.href = project.live;

  const github = document.querySelector("[data-case-github]");
  const privateNote = document.querySelector("[data-case-private]");
  if (github) {
    github.hidden = !project.github;
    if (project.github) github.href = project.github;
  }
  if (privateNote) {
    privateNote.hidden = Boolean(project.github);
    privateNote.textContent = project.github ? "" : labels.private;
  }

  const next = CASE_STUDIES[project.next];
  const nextLink = document.querySelector("[data-case-next]");
  if (nextLink) nextLink.href = `case-study.html?project=${project.next}`;
  setCaseText("[data-case-next-title]", next[locale].title);

  document.title = `${content.title} — Case study — Lazar Minkov`;
  document
    .querySelector('meta[name="description"]')
    ?.setAttribute("content", content.summary);
  document
    .querySelector('link[rel="canonical"]')
    ?.setAttribute("href", `https://lazrm.com/case-study.html?project=${slug}`);
};

window.addEventListener("lazrm:languagechange", (event) => {
  renderCaseStudy(event.detail.language);
});

document.addEventListener("DOMContentLoaded", () => {
  renderCaseStudy(document.documentElement.dataset.language || "en");
});
